const initialProductCount = 6; 
let currentProductCount = initialProductCount; 

document.getElementById("root").innerHTML = renderProducts(product.slice(0, initialProductCount));

function renderProducts(products) {
  return products.map((item, index) => {
    const { image, title, IronPrice, WashAndIronPrice, DryWashPrice } = item;
    const i = index;
    return `<div class='Box'>
      <div class='img-box'>
          <img class='images' src=${image} alt='${title}'></img>
      </div>
      <div class='bottom'>
          <p>${title}</p>
          <div>
              <input type='radio' id='iron${i}' name='options${i}' value='iron'>
              <label for='iron${i}'>Iron (+ ৳${IronPrice})</label><br>
              <input type='radio' id='dryWash${i}' name='options${i}' value='dryWash'>
              <label for='dryWash${i}'>Dry Wash (+ ৳${DryWashPrice})</label><br>
              <input type='radio' id='ironDryWash${i}' name='options${i}' value='ironDryWash'>
              <label for='ironDryWash${i}'>Wash & Iron (+ ৳${WashAndIronPrice})</label><br>
          </div>
          <button onclick='addtocart(${i})'>Add to Laundry</button>
      </div>
    </div>`;
  }).join("");
}

function toggleSeeMore() {
  const seeMoreButton = document.getElementById("seeMoreButton");
  if (currentProductCount === product.length) {
    seeMoreButton.style.display = "none";
  } else {
    currentProductCount += initialProductCount;
    if (currentProductCount >= product.length) {
      currentProductCount = product.length;
      seeMoreButton.style.display = "none";
    }
    document.getElementById("root").innerHTML = renderProducts(product.slice(0, currentProductCount));
  }
}


var cart = [];

function addtocart(a) {
  let selectedProduct = { ...product[a] };
  let selectedOption = document.querySelector(
    `input[name='options${a}']:checked`
  ).value;

  let selectedPrice = 0;
  switch (selectedOption) {
    case "iron":
      selectedPrice = selectedProduct.IronPrice;
      selectedProduct.selectedOption = "Iron";
      break;
    case "dryWash":
      selectedPrice = selectedProduct.WashAndIronPrice;
      selectedProduct.selectedOption = "Dry Wash";
      break;
    case "ironDryWash":
      selectedPrice = selectedProduct.DryWashPrice;
      selectedProduct.selectedOption = "Iron & Dry Wash";
      break;
    default:
      break;
  }

  selectedProduct.selectedPrice = selectedPrice;
  cart.push(selectedProduct);
  displaycart();
}

function displaycart() {
  let total = 0;
  document.getElementById("count").innerHTML = cart.length;

  if (cart.length == 0) {
    document.getElementById("cartItem").innerHTML = "Your cart is empty";
    document.getElementById("total").innerHTML = "৳ 0";
  } else {
    document.getElementById("cartItem").innerHTML = cart
      .map((item) => {
        const { image, title, selectedPrice, selectedOption } = item;
        total += selectedPrice;

        return `<div class='cart-item'>
           <div class='row-img'>
             <img class='rowing' src=${image} alt='${title}'>
           </div>
           <p style='font-size:14px;'>${title}</p>
           <p style='font-size:14px;'>${selectedOption}</p>
           <h2 style='font-size:16px;'>৳ ${selectedPrice}</h2>
           <i class='fa-solid fa-trash' onclick='delElement(${cart.indexOf(
             item
           )})'></i>
        </div>`;
      })
      .join("");

    document.getElementById("total").innerHTML = "৳ " + total;
  }
}

function delElement(index) {
  cart.splice(index, 1);
  displaycart();
}
